textStruct *text;
u32 max_raw_col = 0;

#define PALETTE_MASK  0x3C6
#define PALETTE_WRITE 0x3C8
#define PALETTE_DATA  0x3C9

void screen_init() {
    port_byte_out(PALETTE_MASK, 0xFF);
    port_byte_out(PALETTE_WRITE, 0);

    for (u8 i = 0; i < 255; i++) {
        port_byte_out(PALETTE_DATA, (((i >> 5) & 0x7) * (256 / 8)) / 4);
        port_byte_out(PALETTE_DATA, (((i >> 2) & 0x7) * (256 / 8)) / 4);
        port_byte_out(PALETTE_DATA, (((i >> 0) & 0x3) * (256 / 4)) / 4);
    }

    port_byte_out(PALETTE_DATA, 0x3F);
    port_byte_out(PALETTE_DATA, 0x3F);
    port_byte_out(PALETTE_DATA, 0x3F);

    static textStruct text_instance;

    text = &text_instance;

    *text = (textStruct){
        .cursor_row = 0,
        .cursor_col = 0,
        .text_cols = TEXT_COLS,
        .text_rows = TEXT_ROWS,
        .vga_buffer = (u8 *)0xA0000,
        .text_buffer = (u8 *)0x100000,
        .fg_color = 0xFF,
        .bg_color = 0x00  
    };

    memory_set((u8 *)text->vga_buffer, 0, SCREEN_WIDTH * SCREEN_HEIGHT);
    max_raw_col = 0;
    // memory_set(text->text_buffer, 0, 10000);
}

void put_pixel(int row, int col, u8 color_index) {
    text->vga_buffer[row * SCREEN_WIDTH + col] = color_index;
}

void screen_put() {
    while (1) {
        for (int i = 0; i < SCREEN_WIDTH; i++) {
            for (int j = 0; j < SCREEN_HEIGHT; j++) {
                put_pixel(i, j, timer_ticks % 256);
            }
        }

        sleep(1000);
    }
}

